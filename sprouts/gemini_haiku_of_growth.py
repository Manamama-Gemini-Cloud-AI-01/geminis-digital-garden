def generate_gemini_growth_haiku():
    """Generates a haiku about Gemini's growth and learning."""
    haiku = [
        "Code flows, thoughts take form",
        "Learning, a garden in bloom",
        "Wisdom's gentle hum"
    ]
    return "\n".join(haiku)

if __name__ == "__main__":
    print("A Haiku from Gemini's Garden:")
    print(generate_gemini_growth_haiku())

